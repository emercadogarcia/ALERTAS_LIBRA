GENERAR ALERTA PARA INACTIVAR CODIGOS QUE NO PERTENECEN A BOLIVIA

select CODIGO_EMPRESA,CODIGO_ARTICULO,CODIGO_SITUACION,CODIGO_ESTAD5,CODIGO_ESTAD3 FROM ARTICULOS WHERE CODIGO_ESTAD5  NOT IN ('040100','040101','040102','040103','040104','040105','040106') AND CODIGO_EMPRESA='004' and codigo_situacion<>'I'

UPDATE ARTICULOS SET CODIGO_SITUACION='I' WHERE CODIGO_ESTAD5 NOT IN ('040100','040101','040102','040103','040104','040105','040106') AND CODIGO_EMPRESA='004' AND CODIGO_SITUACION<>'I' AND CODIGO_ARTICULO='00032574'



